package examples.junit.demo;

public class ArithmeticOps {

	public int add(int a, int b) {
		return (a + b);
	}
	
	public int sub(int a, int b) {
		return (a - b);
	}
	
	public int multiply(int a, int b) {
		return (a * b);
	}
	
	public int divide(int a, int b) {
		return (a/b);
	}
	
	public double exp(int a, int b) {
		return (Math.pow(a, b));
	}
	
}
